package var;

public class Concatenacion_cadenas1 {

	public static void main(String[] args) {
		/*
		 * Es un proceso de unir dos o mas cadenas para formar una nueva
		 * el operador + es la forma mas comun para concatenar cadenas
		 * */
		var nombre = "Jesus";
		var apellido = "Yisus";
		var concatenado = nombre+apellido;
		System.out.println("Concatenado => "+concatenado);
	}

}
