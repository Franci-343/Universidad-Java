package var;

public class Reglas_NombresVar1 {

	public static void main(String[] args) {
		System.out.println("============Reglas de nombres de variables============");
		System.out.println("1.- Debe empezar con una letra,simbolo de $ o guion bajo_");
		System.out.println("2.- No puede contener espacios ni caracteres especiales");
		System.out.println("3.- No puede ser una palabra reservada en java");
		System.out.println("4.- Distingue entre mayusculas y minusculas");
		System.out.println();
		System.out.println();
		System.out.println("==========Buenas practicas en nombres de variables==========");
		System.out.println("1.- Usar camel casse (Notacion de camelo)");
		System.out.println("      minuscula y cada nueva palabra inicia con mayuscula");
		System.out.println("2.- Ser descriptivo y claro");
		System.out.println("3.- Prefijos y sufijos claros");
		System.out.println("4.- Evitar nombres de variables de una sola letra");
		System.out.println("5.- No abusar de abreviaturas");
	}

}
